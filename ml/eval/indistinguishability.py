"""Style Indistinguishability Test (the headline research metric).

For each test sample we have:
  - history (the user's past messages)
  - incoming
  - oracle response (generated when LLM had access to the latent style)
  - candidate response (from one of: zero-shot, few-shot, fine-tuned)

We ask an LLM judge to pick which response was written by the actual user.
Bias mitigations:
  - Random A/B order per sample, balanced across the dataset.
  - Generator (oracle) and judge use DIFFERENT providers if both keys are set.
  - Each sample is judged twice with positions swapped; we average accuracy.

Target: ~50% accuracy = the model is indistinguishable from the oracle.
"""
from __future__ import annotations

import asyncio
import math
import os
import random
from dataclasses import dataclass, field

from ml._llm import chat, gather_with_concurrency

# Cap concurrent judge calls — free API tiers enforce a low tokens-per-minute
# limit, so firing every judgment at once just triggers mass rate-limit backoff
# (slower overall). Override with EVAL_CONCURRENCY.
_EVAL_CONCURRENCY = int(os.getenv("EVAL_CONCURRENCY", "3"))

JUDGE_PROMPT = """You are an expert judge of personal WhatsApp writing style.

You will see:
1. A user's chat history (past messages they wrote).
2. A new incoming message to that user.
3. Two candidate responses, A and B. EXACTLY ONE was written by the user; the other was generated by another model.

Decide which was MORE LIKELY written by the same user, based on style cues only:
length, punctuation, emoji habits, slang, formality, rhythm, vocabulary, greeting style.
IGNORE which is "more correct" — judge stylistic resemblance ONLY.

[USER'S CHAT HISTORY]
{history}

[INCOMING MESSAGE]
{incoming}

[RESPONSE A]
{response_a}

[RESPONSE B]
{response_b}

Reply with exactly one character: A or B.
"""


@dataclass
class TrialResult:
    correct: int = 0
    total: int = 0
    correct_oracle_first: int = 0
    total_oracle_first: int = 0
    correct_oracle_second: int = 0
    total_oracle_second: int = 0
    raw: list[dict] = field(default_factory=list)


def _judge_provider() -> str:
    """Pick a DIFFERENT provider for judging when possible (anti-bias).

    Preference order if multiple keys are set: anthropic > openai > groq.
    Falls back to the same provider as the generator if no alternative exists.
    """
    gen = os.getenv("LLM_PROVIDER", "anthropic")
    candidates = []
    if os.getenv("ANTHROPIC_API_KEY"):
        candidates.append("anthropic")
    if os.getenv("OPENAI_API_KEY"):
        candidates.append("openai")
    if os.getenv("GROQ_API_KEY"):
        candidates.append("groq")
    for c in candidates:
        if c != gen:
            return c
    return gen  # fallback: same provider; flag in report


async def judge_pair(history: list[str], incoming: str, oracle: str, model: str, *, oracle_first: bool, provider: str) -> str:
    a, b = (oracle, model) if oracle_first else (model, oracle)
    prompt = JUDGE_PROMPT.format(
        history="\n".join(f"- {h}" for h in history) or "(none)",
        incoming=incoming,
        response_a=a,
        response_b=b,
    )
    out = await chat(prompt, provider=provider, temperature=0.0, max_tokens=4)  # type: ignore[arg-type]
    pick = (out.strip().upper() or "A")[0]
    if pick not in ("A", "B"):
        pick = "A"
    return pick


async def run(samples: list[dict]) -> TrialResult:
    """`samples`: list of {history, incoming, oracle, model}.
    Returns aggregated TrialResult.
    """
    provider = _judge_provider()
    rng = random.Random(0xC0FFEE)
    res = TrialResult()

    async def one(sample: dict) -> None:
        # Two passes for order-bias mitigation
        for oracle_first in (True, False):
            pick = await judge_pair(
                sample["history"],
                sample["incoming"],
                sample["oracle"],
                sample["model"],
                oracle_first=oracle_first,
                provider=provider,
            )
            picked_oracle = (pick == "A" and oracle_first) or (pick == "B" and not oracle_first)
            res.total += 1
            if picked_oracle:
                res.correct += 1
            if oracle_first:
                res.total_oracle_first += 1
                if picked_oracle:
                    res.correct_oracle_first += 1
            else:
                res.total_oracle_second += 1
                if picked_oracle:
                    res.correct_oracle_second += 1
            res.raw.append(
                {
                    "user_id": sample.get("user_id"),
                    "incoming": sample["incoming"],
                    "oracle": sample["oracle"],
                    "model": sample["model"],
                    "oracle_first": oracle_first,
                    "judge_pick": pick,
                    "picked_oracle": picked_oracle,
                }
            )

    rng.shuffle(samples)
    await gather_with_concurrency([one(s) for s in samples], _EVAL_CONCURRENCY)
    return res


def accuracy(r: TrialResult) -> float:
    return r.correct / r.total if r.total else 0.0


def wilson_ci(n: int, k: int, z: float = 1.96) -> tuple[float, float]:
    """Wilson 95% CI for a proportion. Better than normal approx for small n."""
    if n == 0:
        return (0.0, 0.0)
    p = k / n
    denom = 1 + z * z / n
    center = (p + z * z / (2 * n)) / denom
    half = z * math.sqrt((p * (1 - p) + z * z / (4 * n)) / n) / denom
    return max(0.0, center - half), min(1.0, center + half)
